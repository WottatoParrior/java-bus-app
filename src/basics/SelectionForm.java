    private void ShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowActionPerformed
        // TODO add your handling code here:
        String res =stoplist.getSelectedValue();
        stopInfo.setText("");
        String[] tokens= res.split("-"); //        Παιρνω το κομματι που θελω

        try {
            ArrayList trips =DB.fetchFromDbInfo(tokens[0]); //Παιρνω το τριπ ID απο την επιλογη του χρηστη
            for(Object route : trips){                      //Για καθε ενα τριπ παιρνω το αντιστοιχω route id
                String convertedToString = route.toString();
                ArrayList<String> id = DB.fetchRoutes(convertedToString);
                for(String i :id){
                   String name = DB.showRoutes(i);          //Για καθε route id παω στην βαση μου και παιρνω τα στοιχεια της στασης και τα εμφανιζω
                   stopInfo.append(name + "\n");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(SelectionForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_ShowActionPerformed

