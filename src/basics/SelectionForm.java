    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String UserInput = stopName.getText();
        this.datamodel.removeAllElements();
        DB.connect();
        try {
           LinkedHashMap<String, String> found = DB.fetchFromDb(UserInput);
           for(String value : found.values()){
               this.datamodel.addElement(value);
               
           }
           
        } catch (SQLException ex) {
            Logger.getLogger(SelectionForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        stopName.setText("");
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

